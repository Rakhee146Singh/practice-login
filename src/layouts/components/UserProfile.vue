<script setup>
import avatar1 from '@images/avatars/avatar-1.png';
import { onMounted, ref } from 'vue';
import { useRouter } from 'vue-router';

const router = useRouter();
const userName = ref(''); // Holds the user name
const userRole = ref(''); // Holds the user role (default: Admin)

const loadUserData = () => {
  const savedUserData = JSON.parse(localStorage.getItem('user-info'));
  if (savedUserData && Array.isArray(savedUserData) && savedUserData.length > 0) {
    const user = savedUserData[0];
    userName.value = user.name || 'User'; // Default to 'User' if name is missing
    userRole.value = 'Admin'; // Default to 'Admin' if role is missing
  }
};

const logout = () => {
  localStorage.removeItem('user-info');
  router.push('/login');
};

onMounted(loadUserData);
</script>

<template>
  <VBadge dot location="bottom right" offset-x="3" offset-y="3" bordered color="success">
    <VAvatar class="cursor-pointer" color="primary" variant="tonal">
      <VImg :src="avatar1" />

      <!-- SECTION Menu -->
      <VMenu activator="parent" width="230" location="bottom end" offset="14px">
        <VList>
          <!-- ðŸ‘‰ User Avatar & Name -->
          <VListItem>
            <template #prepend>
              <VListItemAction start>
                <VBadge dot location="bottom right" offset-x="3" offset-y="3" color="success">
                  <VAvatar color="primary" variant="tonal">
                    <VImg :src="avatar1" />
                  </VAvatar>
                </VBadge>
              </VListItemAction>
            </template>

            <VListItemTitle class="font-weight-semibold">
              {{ userName }}
            </VListItemTitle>
            <VListItemSubtitle>{{ userRole }}</VListItemSubtitle>
          </VListItem>

          <VDivider class="my-2" />

          <!-- ðŸ‘‰ Profile -->
          <VListItem to="/profile">
            <template #prepend>
              <VIcon class="me-2" icon="tabler-user" size="22" />
            </template>

            <VListItemTitle>Profile</VListItemTitle>
          </VListItem>

          <!-- ðŸ‘‰ Settings -->
          <VListItem link>
            <template #prepend>
              <VIcon class="me-2" icon="tabler-settings" size="22" />
            </template>

            <VListItemTitle>Settings</VListItemTitle>
          </VListItem>

          <!-- ðŸ‘‰ Pricing -->
          <VListItem link>
            <template #prepend>
              <VIcon class="me-2" icon="tabler-currency-dollar" size="22" />
            </template>

            <VListItemTitle>Pricing</VListItemTitle>
          </VListItem>

          <!-- ðŸ‘‰ FAQ -->
          <VListItem link>
            <template #prepend>
              <VIcon class="me-2" icon="tabler-help" size="22" />
            </template>

            <VListItemTitle>FAQ</VListItemTitle>
          </VListItem>

          <!-- Divider -->
          <VDivider class="my-2" />

          <!-- ðŸ‘‰ Logout  @click="logout"-->
          <VListItem @click="logout">
